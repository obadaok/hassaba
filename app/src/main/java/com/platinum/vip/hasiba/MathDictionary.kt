    // مولد الأرقام المركبة الذكي (من 21 إلى 99) لتجنب كتابتها يدوياً
    private val compoundNumbers = mutableMapOf<String, String>().apply {
        val units = mapOf(
            "واحد" to 1, "اثنين" to 2, "اتنين" to 2, "ثلاثة" to 3, "تلاتة" to 3, "تلاته" to 3,
