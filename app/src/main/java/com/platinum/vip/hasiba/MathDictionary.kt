            "ثمانية" to 8, "تمانية" to 8, "تمانيه" to 8, "تسعة" to 9, "تسعه" to 9
        )
        val tens = mapOf(
            "عشرين" to 20, "عشرون" to 20, "ثلاثين" to 30, "تلاتين" to 30, "ثلاثون" to 30,
            "أربعين" to 40, "اربعين" to 40, "أربعون" to 40, "خمسين" to 50, "خمسون" to 50,
            "ستين" to 60, "ستون" to 60, "سبعين" to 70, "سبعون" to 70,
            "ثمانين" to 80, "تمانين" to 80, "ثمانون" to 80, "تسعين" to 90, "تسعون" to 90
        )

        // توليد كل الاحتمالات مثل "خمسة وعشرين"، "خمسه و عشرين"، إلخ...
        for ((uName, uVal) in units) {
            for ((tName, tVal) in tens) {
                val totalValue = (uVal + tVal).toString()
                this["$uName و$tName"] = totalValue
                this["$uName و $tName"] = totalValue
            }
        }
    }

    fun convertToMathExpression(spokenText: String): String {
        // 1. تنظيف المسافات الزائدة الأولية (مهمة لنجاح البحث في القاموس)
        var result = spokenText.trim().replace(Regex("\\s+"), " ")

        // 2. استبدال الأعداد المركبة أولاً (ترتيب تنازلي حسب الطول لضمان الدقة)
        compoundNumbers.entries.sortedByDescending { it.key.length }.forEach { (word, number) ->
            result = result.replace(word, number, ignoreCase = true)
        }

        // 3. استبدال باقي الكلمات من القاموس (ترتيب تنازلي حسب الطول)
        wordToSymbol.entries.sortedByDescending { it.key.length }.forEach { (word, symbol) ->
            result = result.replace(word, symbol, ignoreCase = true)
        }

        // 4. حذف جـمـيـع المسافات نهائياً (بناءً على طلبك)
        result = result.replace(" ", "")

        // 5. تنظيف الرموز المكررة
        result = result.replace(Regex("([+\\-×÷%])\\1+"), "$1")

        // 6. إزالة أي رموز عمليات حسابية جاءت بالخطأ في بداية أو نهاية النص
        // لا نمسح '-' من البداية لأنه قد يكون علامة سالبة (ناقص خمسة = -5)
        result = result.trimStart('+', '×', '÷', '%', '.', '=').trimEnd('+', '-', '×', '÷', '%', '.', '=')
        // إزالة أي علامات يساوي متبقية في وسط التعبير (ليس لها معنى حسابي)
        result = result.replace("=", "")

        // 7. التحقق من أن النتيجة تحتوي فقط على رموز رياضية صالحة
        if (result.isNotEmpty() && !result.matches(Regex("^[\\d+\\-×÷%.()]+$"))) {
            return ""
        }

        return result
    }
